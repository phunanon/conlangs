function word2pipu (word)
{
    word = word.toLowerCase();
    if (word.match(/\d+/) != null) { //It's a number
        var num = parseInt(word);
        for (l in pipu_lex) {
            if (pipu_lex[l].num == num) {
                return l + gloss_pipu["num"];
            }
        }
    } else {
        for (l in pipu_lex) {
            var roots = pipu_lex[l].root.split('/');
            for (r in roots) {
                if (roots[r] == word) {
                    return pipu_lex[l].pipu;
                }
            }
        }
    }
    return '?';
}


function gloss2pipu (gloss)
{
    gloss = gloss.split(" ");
    var pipu = "";
    for (g in gloss) {
        var gl = gloss[g].split("-");
        var grammar = (gl.length > 1 ? gl[1] : 'root');
        if (grammar == "prop") { //Is a proper noun
            pipu += ' '+ gl[0];
        } else {
            pipu += ' '+ word2pipu(gl[0]) + gloss_pipu[grammar];
        }
    }
    return pipu.trim();
}



